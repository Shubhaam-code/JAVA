import java.util.*;

public class SwitchExample {
    public static void main(String arr[]) {
        Scanner sc = new Scanner(System.in);
        System.out.print("ENTER THE VALUE OF A :");
        int a = sc.nextInt();

        switch (a) {
            case 1:
                System.out.println("lol");
                break;
            default:
                System.out.println("cool");
               

        }
        sc.close();

    }

}
